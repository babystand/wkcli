let convertPart (str : string) =
    match  str.ToLower() with 
    | "a" -> "あ"
    | "i" -> "い"
    | "u" -> "う"
    | "e" -> "え"
    | "o" -> "お"
    | "ka" -> "か"
    | "ki" -> "き"
    | "ku" -> "く"
    | "ke" -> "け"
    | "ko" -> "こ"
    | "ga" -> "が"
    | "gi" -> "ぎ"
    | "gu" -> "ぐ"
    | "ge" -> "げ"
    | "go" -> "ご"
    | "sa" -> "さ"
    | "si" -> "し"
    | "shi" -> "し"
    | "su" -> "す"
    | "se" -> "せ"
    | "so" -> "そ"    
    | "za" -> "ざ"
    | "zi" -> "じ"
    | "zhi" -> "じ"
    | "zu" -> "ず"
    | "ze" -> "ぜ"
    | "zo" -> "ぞ"
    | "ta" -> "か"
    | "ti" -> "ち"
    | "chi" -> "ちに"
    | "tu" -> "つ"
    | "tsu" -> "つ"
    | "te" -> "て"
    | "to" -> "と"
    | "da" -> "だ"
    | "di" -> "ぢ"
    | "du" -> "づ"
    | "dsu" -> "づ"
    | "dzu" -> "づ"
    | "de" -> "で"
    | "do" -> "ど"
    | "na" -> "な"
    | "ni" -> "に"
    | "nu" -> "ぬ"
    | "ne" -> "ね"
    | "no" -> "の"
    | "ha" -> "は"
    | "hi" -> "ひ"
    | "hu" -> "ふ"
    | "fu" -> "ふ"
    | "he" -> "へ"
    | "ho" -> "ほ"    
    | "ba" -> "ば"
    | "bi" -> "び"
    | "bu" -> "ぶ"
    | "be" -> "べ"
    | "bo" -> "ぼ"  
    | "pa" -> "ぱ"
    | "pi" -> "ぴ"
    | "pu" -> "ぷ"
    | "pe" -> "ぺ"
    | "po" -> "ぽ"  
    | "ma" -> "ま"
    | "mi" -> "み"
    | "mu" -> "む"
    | "me" -> "め"
    | "mo" -> "も"    
    | "ya" -> "や"
    | "yu" -> "ゆ"
