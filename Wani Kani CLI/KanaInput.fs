    | "kya" -> "きゃ"
    | "kyu" -> "きゅ"
    | "kyo" -> "きょ"
    | "sha" -> "しゃ"
    | "shu" -> "しゅ"
    | "sho" -> "しょ"
    | "cha" -> "ちゃ"
    | "chu" -> "ちゅ"
    | "cho" -> "ちょ"
    | "nya" -> "にゃ"
    | "nyu" -> "にゅ"
    | "nyo" -> "にょ"
    | "hya" -> "ひゃ"
    | "hyu" -> "ひゅ"
    | "hyo" -> "ひょ"
    | "mya" -> "みゃ"
    | "myu" -> "みゅ"
    | "myo" -> "みょ"
    | "rya" -> "りゃ"
    | "ryu" -> "りゅ"
    | "ryo" -> "りょ"
    | "gya" ->"ぎゃ"
    | "gyu" ->"ぎゅ"
    | "gyo" ->"ぎょ"
    | "ja" -> "じゃ"
    | "ju" -> "じゅ"
    | "jo" -> "じょ"
    | "bya" ->"びゃ"
    | "byu"->"びゅ"
    | "byo"->"びょ"
    | "pya"->"ぴゃ"
    | "pyu"->"ぴゅ"
    | "pyo"->"ぴょ"
    | (x : string) when x.Length > 1 && x.Chars 0 = x.Chars 1  -> "っ"+ x.Substring 1
    | _ -> str
    
